int		ft_strcmp(char *s1, char *s2)
{
	int s;

	s = 0;
	while (s1[s] == s2[s] && s1[s] != '\0' && s2[s] != '\0')
		s++;
	if (s1[s] != s2[s])
		return ((unsigned char)s1[s] - (unsigned char)s2[s]);
	return (0);
}
